    public double FR48(String name) {
        double variable = 0;
        switch(name){

            case "p":
                variable = 5.0E-5;
                break;
            case "i":
                variable = 1.0E-6;
                break;
            case "d":
                variable = 0.0;
                break;
            case "iz":
                variable = 0.0;
                break;
            case "ff":
                variable = 0.0;
                break;
            case "max":
                variable = 1.0;
                break;
            case "min":
                variable = -1.0;
                break;
        }
        return variable;
    }
   
